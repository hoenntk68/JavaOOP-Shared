public class Teacher {
        String id;
    
    Teacher(String id){
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "Teacher{" + "id=" + id + '}';
    }
    
    
}
